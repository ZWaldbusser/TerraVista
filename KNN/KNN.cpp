/*

KNN: Classifier model
    Works by finding the most similar data points in the data, and it attempts to make an educated guess based on their classifications


    For example: Classifying someones clothing size based on their measurements

How it works:
    Select a value k
    Calculate the distance from the point to every other point in the set
    Pick the k closest points
    Run a vote with the k closest points, the point is classified on the winner.





*/